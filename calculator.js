let first = 3;
let second = 5;
let operation;

operation = "multiplication";

if (operation === "addition") {
  console.log(`${first} + ${second} = ${first + second}`);
} else if (operation === "subtraction") {
  console.log(`${first} - ${second} = ${first - second}`);
} else if (operation === "multiplication") {
  console.log(`${first} * ${second} = ${first * second}`);
} else if (operation === "division") {
  console.log(`${first} / ${second} = ${first / second}`);
}
